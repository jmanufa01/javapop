    /**
     * 
     * Metodo el cual comprueba los credenciales introducidos y si son correctos nos devuelve el usuario que ha
     * iniciado sesion
     * 
     * 
     * 
     * @param correo
     * @param contraseña
     * @return 
     */
    
    public Usuario LogIn(String correo,String contraseña){ 
        if(correo.equals("admin@javapop.com") && contraseña.equals("admin")){
            for(Usuario cadaUsuario: UtilidadJavaPop.getUsuarios()){
                if(cadaUsuario.isAdmin()){
                    JOptionPane.showMessageDialog(null, 
                            "Bienvenido de nuevo administrador!","Bienvenido",JOptionPane.INFORMATION_MESSAGE);
                    return cadaUsuario;
                }
            }      
        }else{
            for(Usuario cadaUsuario : UtilidadJavaPop.getUsuarios()){
                if(cadaUsuario.getCorreo().equals(correo) && cadaUsuario.getContraseña().equals(contraseña) &&
                        !cadaUsuario.isAdmin()){
                    JOptionPane.showMessageDialog(null, 
                            "Ha iniciado sesion correctamente","Bienvenido",JOptionPane.INFORMATION_MESSAGE);
                    return cadaUsuario;
                }                
            }
        }
        JOptionPane.showMessageDialog(null, "Correo y/o contraseña incorrectos", "Error de registro", JOptionPane.WARNING_MESSAGE);
        return null;
    }

