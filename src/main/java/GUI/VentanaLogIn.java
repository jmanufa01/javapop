    private void logInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInButtonActionPerformed
        char [] arrayC = contraseñaTextField.getPassword();
        String password = new String(arrayC);
        
        
        Usuario usuario =  LogIn(correoTextField.getText(), password);
        
        if(!usuario.isAdmin()){
            Cliente cliente=(Cliente)usuario;
            cliente.cargarProductosComprados();
            cliente.cargarProductosVendidos();
            cliente.cargarProductosEnVenta();
            this.ventanaPrincipal.getMenuAdmin().setVisible(false);
            this.ventanaPrincipal.setVisible(true);
            this.ventanaPrincipal.setUsuarioLoggeado(usuario);
            ventanaPrincipal.getPanelPrincipal().removeAll();
            ventanaPrincipal.getPanelPrincipal().add(new PanelInicial(this.ventanaPrincipal));
            ventanaPrincipal.getPanelPrincipal().revalidate();
            ventanaPrincipal.getPanelPrincipal().repaint();
            ventanaPrincipal.getPanelPrincipal().updateUI();
            this.dispose();
        }else{
           this.ventanaPrincipal.getMenuCliente().setVisible(false);
           this.ventanaPrincipal.setVisible(true);
           this.ventanaPrincipal.setUsuarioLoggeado(usuario);
           ventanaPrincipal.getPanelPrincipal().removeAll();
           ventanaPrincipal.getPanelPrincipal().add(new PanelInicial(this.ventanaPrincipal));
           ventanaPrincipal.getPanelPrincipal().revalidate();
           ventanaPrincipal.getPanelPrincipal().repaint();
           ventanaPrincipal.getPanelPrincipal().updateUI();
           this.dispose();
        } 
    }//GEN-LAST:event_logInButtonActionPerformed

