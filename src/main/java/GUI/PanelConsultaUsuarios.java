    /**
     * 
     * Metodo el cual rellena el JList con los datos de todos los usuarios registrados
     * 
     * 
     */
    public void rellenarLista(){
        
        //Crear un objeto DefaultListModel
        DefaultListModel listModel = new DefaultListModel();
        //Recorrer el contenido del ArrayList
        for(int i=0; i<UtilidadJavaPop.getUsuarios().size(); i++) {
            //Añadir cada elemento del ArrayList en el modelo de la lista
            listModel.add(i, UtilidadJavaPop.getUsuarios().get(i).getCorreo());
        }
        //Asociar el modelo de lista al JList
        usuariosList.setModel(listModel);
        usuariosList.repaint();
        usuariosList.updateUI();
    }
    
    
