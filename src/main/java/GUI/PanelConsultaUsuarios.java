    private void usuariosListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_usuariosListMouseClicked
        
        Usuario usuarioExt = null;
        for(Usuario cadaUsuario : UtilidadJavaPop.getUsuarios()){
            if(cadaUsuario.getCorreo().equals(usuariosList.getSelectedValue())){
                usuarioExt = cadaUsuario;
                
            }
            
        }
        vaciar();
        correoTextField.setText(usuarioExt.getCorreo());
        clientePanel.setVisible(false);
        clienteProfPanel.setVisible(false);
        if(!usuarioExt.isAdmin()){
            Cliente cliente =(Cliente)usuarioExt;
            dniTextField.setText(cliente.getDni());
            nombreTextField.setText(cliente.getNombre());
            ubicacionTextField.setText(cliente.getUbicacion().getCiudad()+
                    ", "+cliente.getUbicacion().getCodigoPostal());
            tarjetaTextField.setText(cliente.getTarjetaCredito());
            clientePanel.setVisible(true);
            if(usuarioExt.getClass().equals(ClienteProfesional.class)){
                ClienteProfesional clienteProf = (ClienteProfesional) cliente;
                descripcionTextArea.setText(clienteProf.getDescripion());
                telefonoTextField.setText(String.valueOf(clienteProf.getTelefono()));
                webTextField.setText(clienteProf.getWeb());
                horarioTextField.setText(clienteProf.getHorario());
                clienteProfPanel.setVisible(true);
            }
                
        }
        tipoClienteLabel.setText(usuarioExt.getClass().getSimpleName());
        this.repaint();
        this.revalidate();
        this.updateUI();
        
        
        
        
        
        
        
    }//GEN-LAST:event_usuariosListMouseClicked

    private void bajaClienteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bajaClienteButtonActionPerformed
        try{
            String botones[]={"Sí","No"};
            if(usuariosList.getSelectedValue()==null){
                JOptionPane.showMessageDialog(null,"No ha seleccionado ningún usuario", "Error!", JOptionPane.ERROR_MESSAGE);
            }else{
                Administrador admin = (Administrador) ventanaPrincipal.getUsuarioLoggeado();
                if(usuariosList.getSelectedValue().equals(admin.getCorreo())){
                    JOptionPane.showMessageDialog(null,"No puede eliminar al Administrador", "Error!", JOptionPane.ERROR_MESSAGE);
                }else{
                    int eleccion = JOptionPane.showOptionDialog(this, "¿Desea eliminarlo?", "Informacion", 0,0,null,botones,null);         
                    if(eleccion == JOptionPane.YES_OPTION){
                        admin.bajaCliente(correoTextField.getText());
                        UtilidadJavaPop.guardarUsuarios();
                        UtilidadJavaPop.guardarProductos();
                        correoTextField.setText("");
                        descripcionTextArea.setText("");
                        dniTextField.setText("");
                        telefonoTextField.setText("");
                        nombreTextField.setText("");
                        ubicacionTextField.setText("");
                        tarjetaTextField.setText("");
                        horarioTextField.setText("");
                        webTextField.setText("");
                        rellenarLista();
                        this.revalidate();
                        this.repaint();
                        this.updateUI();
                    }
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage(), "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bajaClienteButtonActionPerformed


