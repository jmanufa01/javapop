    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            Cliente cliente = (Cliente)ventanaprincipal.getUsuarioLoggeado();
            String botones[]={"Sí","No"};
            if(productosList.getSelectedValue()==null){
                JOptionPane.showMessageDialog(null,"No ha seleccionado ningún producto", "Error!", JOptionPane.ERROR_MESSAGE);
            }else{
                int eleccion = JOptionPane.showOptionDialog(this, "¿Desea eliminarlo?", "Informacion", 0,0,null,botones,null);         
                if(eleccion == JOptionPane.YES_OPTION){
                    for(Producto cadaProducto : cliente.getProductosEnVenta()){
                        if(cadaProducto.getTitulo().equals(productosList.getSelectedValue())){
                            cliente.eliminarProductoCliente(cadaProducto);
                            cliente.guardarProductosEnVenta();
                            UtilidadJavaPop.guardarProductos();
                            tituloTextField1.setText("");
                            descripcionTextArea.setText("");
                            precioTextField1.setText("");
                            categoriaTextField.setText("");
                            estadoTextField.setText("");
                            rellenarLista();
                            this.revalidate();
                            this.repaint();
                            this.updateUI();
                            break;

                        }
                    }
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage(), "Error!", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

