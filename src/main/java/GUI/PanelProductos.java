    /**
     * 
     * Metodo el cual rellena el JList con los productos que tiene el cliente en venta actualmente
     * 
     * 
     */
    
    public void rellenarLista(){
        Cliente cliente = (Cliente)ventanaprincipal.getUsuarioLoggeado();
        //Crear un objeto DefaultListModel
        DefaultListModel listModel = new DefaultListModel();
        //Recorrer el contenido del ArrayList
        for(int i=0; i<cliente.getProductosEnVenta().size(); i++) {
            //Añadir cada elemento del ArrayList en el modelo de la lista
            listModel.add(i, cliente.getProductosEnVenta().get(i).getTitulo());
        }
        //Asociar el modelo de lista al JList
        productosList.setModel(listModel);
        productosList.repaint();
        productosList.updateUI();
    }
    
    
    /**
     * 
     * 
     * 
     * Metodo el cual rellena la informacion del producto en los distintos TextField
     * 
     * 
     * @param producto 
     */
    
    public void rellenarInfo(Producto producto){
        
        tituloTextField1.setText(producto.getTitulo());
        precioTextField1.setText(String.valueOf(producto.getPrecio())+" €");
        estadoTextField.setText(producto.getEstado().toString());
        categoriaTextField.setText(producto.toStringCategoria());
        descripcionTextArea.setText(producto.getDescripcion());
        ImageIcon imagen = producto.getFotografia();
        ImageIcon imgRedim = new ImageIcon(imagen.getImage().getScaledInstance(jLabel10.getWidth(), jLabel10.getHeight(), Image.SCALE_SMOOTH));
        jLabel10.setIcon(imgRedim);
                
        this.revalidate();
        this.repaint();
        this.updateUI();
        
        
    }
    


